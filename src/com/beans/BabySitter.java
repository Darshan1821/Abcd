package com.beans;

public class BabySitter {

	private long bsId;
	private String bsName;
	private int bsAge;
	private int bseEperience;
	private String bsType;
	private String bsStatus;

	public long getBsId() {
		return bsId;
	}

	public void setBsId(long bsId) {
		this.bsId = bsId;
	}

	public String getBsName() {
		return bsName;
	}

	public void setBsName(String bsName) {
		this.bsName = bsName;
	}

	public int getBsAge() {
		return bsAge;
	}

	public void setBsAge(int bsAge) {
		this.bsAge = bsAge;
	}

	public int getBseEperience() {
		return bseEperience;
	}

	public void setBseEperience(int bseEperience) {
		this.bseEperience = bseEperience;
	}

	public String getBsType() {
		return bsType;
	}

	public void setBsType(String bsType) {
		this.bsType = bsType;
	}

	public String getBsStatus() {
		return bsStatus;
	}

	public void setBsStatus(String bsStatus) {
		this.bsStatus = bsStatus;
	}

}
